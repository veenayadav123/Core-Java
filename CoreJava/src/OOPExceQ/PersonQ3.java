package oopExceQ;

public class PersonQ3 {

	private String fname;
	private String lname;
	
	public PersonQ3(String fname, String lname) {

		this.fname = fname;
		this.lname = lname;
	}

	public String getfname() {

		return fname;
	}

	public String getlname() {

		return lname;
	}

	

	public String toString() {
		
		return fname+" "+lname;
	}

}
