package javabasics;

public class NewMath {
	public static void main(String[] args) {
		System.out.println("Mathematics Operations");
		System.out.println("Max 25,61- " +Math.max(25,61));
		System.out.println("Min 25,61- " +Math.min(25,61));
		System.out.println("Absolute 3.8- " +Math.abs(3.8));
		System.out.println("Exp 5 - " +Math.exp(5));
		System.out.println("Random Number - " +Math.random());
		System.out.println("Square Root - " +Math.sqrt(625));
	}

}
