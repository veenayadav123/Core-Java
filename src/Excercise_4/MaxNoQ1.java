package Excercise_4;

public class MaxNoQ1 {
	public static void main(String[] args) {
		
		int m=Math.max(5,7);
		System.out.println("Maximum number is = "+m);
		
		int i=Integer.parseInt(args[0]);
		int j=Integer.parseInt(args[1]);
		int n=Math.max(i,j);
		System.out.println("Maximum number is = "+n);
		
	}

}
